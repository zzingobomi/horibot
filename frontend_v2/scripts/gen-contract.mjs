// Contract type generator — 떠 있는 backend 의 GET /contract.json 을 HTTP 로 가져와
// contract.ts 를 emit 하는 **frontend 빌드 도구**.
//
// frontend_contract_gen.md §6. 경계: backend 는 자기 계약을 /contract.json 으로
// EXPORT, frontend 는 그 계약 artifact 만 CONSUME. 이 스크립트는 backend 코드/폴더/
// python 환경을 일절 건드리지 않는다 (openapi-typescript http://.../openapi.json 와
// 동일 패턴 — 도구가 떠 있는 서버의 계약을 HTTP 로 소비).
//
// 실행 전 backend 를 전 module 로드하는 host 로 띄워야 함 (mock / dev):
//     cd backend_v2 && uv run python -m apps.main --host mock   # bridge :8000
//
// usage (pnpm gen:types):
//     node scripts/gen-contract.mjs [--url http://localhost:8000/contract.json]
//                                   [--out src/api/generated/contract.ts]

import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { pathToFileURL } from "node:url";

const DEFAULT_URL = "http://localhost:8000/contract.json";
const DEFAULT_OUT = "src/api/generated/contract.ts";

export async function fetchContract(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`GET ${url} → HTTP ${res.status}`);
  return res.json();
}

// contract JSON → contract.ts 문자열. 순수 함수 (I/O 없음). type 문자열은 backend
// (apps/contract_export.py) 가 실 Python type 을 알고 이미 TS 로 해소해 둠 — 여기선
// 문자열 조립만 (JSON schema→TS 재해석 X). JSON.stringify = TS 문자열 리터럴과 동일
// escape (ASCII 키/값 기준).
export function renderContractTs(data) {
  const lines = [];
  lines.push("/**");
  lines.push(" * Auto-generated by frontend_v2/scripts/gen-contract.mjs");
  lines.push(" * Source: running backend GET /contract.json");
  lines.push(
    " * frontend 노출 = apps/contract_export.py::FRONTEND_EXPOSED (그 subset).",
  );
  lines.push(" * DO NOT EDIT - run `pnpm gen:types` to regenerate.");
  lines.push(" */");
  lines.push("");

  // ─── StrEnum (const + value union) ───────────────────────────
  for (const en of data.enums) {
    const name = en.name;
    lines.push(`export const ${name} = {`);
    for (const [mname, mvalue] of en.members) {
      lines.push(`  ${mname}: ${JSON.stringify(mvalue)},`);
    }
    lines.push("} as const;");
    lines.push(
      `export type ${name}Value = (typeof ${name})[keyof typeof ${name}];`,
    );
    lines.push("");
  }

  // ─── BaseModel interface ─────────────────────────────────────
  for (const iface of data.interfaces) {
    lines.push(`export interface ${iface.name} {`);
    for (const f of iface.fields) {
      const sep = f.optional ? "?:" : ":";
      lines.push(`  ${f.name}${sep} ${f.ts};`);
    }
    lines.push("}");
    lines.push("");
  }

  // ─── Topic (Stream + Event) + TopicPayloadMap ────────────────
  lines.push("export const Topic = {");
  for (const t of data.topics) {
    lines.push(`  ${t.const}: ${JSON.stringify(t.key)},`);
  }
  lines.push("} as const;");
  lines.push("export type TopicKey = (typeof Topic)[keyof typeof Topic];");
  lines.push("");

  lines.push("export type TopicPayloadMap = {");
  for (const t of data.topics) {
    lines.push(`  ${JSON.stringify(t.key)}: ${t.payload};`);
  }
  lines.push("};");
  lines.push("");

  // ─── ServiceKey + ServiceMap ─────────────────────────────────
  lines.push("export const ServiceKey = {");
  for (const s of data.services) {
    lines.push(`  ${s.const}: ${JSON.stringify(s.key)},`);
  }
  lines.push("} as const;");
  lines.push(
    "export type ServiceKeyValue = (typeof ServiceKey)[keyof typeof ServiceKey];",
  );
  lines.push("");

  lines.push("export type ServiceMap = {");
  for (const s of data.services) {
    lines.push(`  ${JSON.stringify(s.key)}: { req: ${s.req}; res: ${s.res} };`);
  }
  lines.push("};");
  lines.push("");

  return lines.join("\n");
}

function parseArgs(argv) {
  const out = { url: DEFAULT_URL, out: DEFAULT_OUT };
  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === "--url") out.url = argv[++i];
    else if (argv[i] === "--out") out.out = argv[++i];
  }
  return out;
}

async function main() {
  const { url, out } = parseArgs(process.argv.slice(2));
  let data;
  try {
    data = await fetchContract(url);
  } catch (e) {
    console.error(
      `[gen-contract] ${url} fetch 실패: ${e.message}\n` +
        "  backend 를 전 module 로드하는 host 로 먼저 띄워야 함 " +
        "(cd backend_v2 && uv run python -m apps.main --host mock).",
    );
    process.exit(2);
  }

  const ts = renderContractTs(data);
  const outPath = resolve(process.cwd(), out);
  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, ts, { encoding: "utf-8" });

  console.log(
    `[gen-contract] ${outPath} -- interfaces ${data.interfaces.length}, ` +
      `topics ${data.topics.length}, services ${data.services.length} ` +
      `(source: ${url})`,
  );
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  await main();
}
