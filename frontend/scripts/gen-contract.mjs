#!/usr/bin/env node
/**
 * `/openapi.json` 의 `x-contract` vendor extension 을 읽어
 * `src/api/generated/contract.ts` 를 생성.
 *
 * Contract = backend `api_contract.py` 의 PUBLIC_TOPICS / PUBLIC_SERVICES
 * 가 인라인된 mapping. 생성물:
 *   - Topic / ServiceKey 상수
 *   - TopicPayloadMap (subscribe 인자 typed)
 *   - ServiceMap (callService 인자/응답 typed)
 *   - BINARY_TOPICS 상수
 *
 * 사용: `pnpm gen:types` 가 openapi-typescript 다음에 호출.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const OPENAPI_URL =
  process.env.OPENAPI_URL ?? "http://localhost:8000/openapi.json";
const OPENAPI_FILE = process.env.OPENAPI_FILE; // 검증/오프라인 모드
const OUT_PATH = resolve(process.cwd(), "src/api/generated/contract.ts");

function payloadType(name) {
  if (name === null) return "unknown"; // free-form dict
  // pydantic v2 EmptyData (필드 0) 는 OpenAPI components/schemas 에 별도
  // 등재 안 됨 → inline 처리. 호출 시 `{}` 빈 객체 보내라는 의미.
  if (name === "EmptyData") return "Record<string, never>";
  return `components["schemas"]["${name}"]`;
}

async function loadOpenApi() {
  if (OPENAPI_FILE) {
    return JSON.parse(readFileSync(OPENAPI_FILE, "utf-8"));
  }
  const res = await fetch(OPENAPI_URL);
  if (!res.ok) {
    throw new Error(`openapi fetch 실패: ${res.status} ${res.statusText}`);
  }
  return await res.json();
}

async function main() {
  const openapi = await loadOpenApi();
  const xc = openapi["x-contract"];
  if (!xc) {
    throw new Error(
      "/openapi.json 에 `x-contract` 키 없음 — backend bridge 의 " +
        "`custom_openapi()` 가 등록됐는지 확인.",
    );
  }

  const lines = [];
  lines.push("/**");
  lines.push(" * Auto-generated by frontend/scripts/gen-contract.mjs");
  lines.push(" * Source: backend/api_contract.py (via /openapi.json x-contract)");
  lines.push(" * DO NOT EDIT — run `pnpm gen:types` to regenerate.");
  lines.push(" */");
  lines.push(`import type { components } from "./types";`);
  lines.push("");

  // ─── Topic 상수 ───────────────────────────────────────
  lines.push("export const Topic = {");
  for (const [key, info] of Object.entries(xc.topics)) {
    lines.push(`  ${info.name}: ${JSON.stringify(key)},`);
  }
  for (const { key, name } of xc.binary_topics) {
    lines.push(`  ${name}: ${JSON.stringify(key)},`);
  }
  lines.push("} as const;");
  lines.push("export type TopicKey = (typeof Topic)[keyof typeof Topic];");
  lines.push("");

  // ─── Binary topic set ────────────────────────────────
  lines.push("export const BINARY_TOPICS: ReadonlySet<string> = new Set([");
  for (const { key } of xc.binary_topics) {
    lines.push(`  ${JSON.stringify(key)},`);
  }
  lines.push("]);");
  lines.push("");

  // ─── TopicPayloadMap (JSON 토픽만; binary 는 별도 트랙) ──
  lines.push("export type TopicPayloadMap = {");
  for (const [key, info] of Object.entries(xc.topics)) {
    lines.push(`  ${JSON.stringify(key)}: ${payloadType(info.payload)};`);
  }
  lines.push("};");
  lines.push("");

  // ─── ServiceKey 상수 ─────────────────────────────────
  lines.push("export const ServiceKey = {");
  for (const [key, info] of Object.entries(xc.services)) {
    lines.push(`  ${info.name}: ${JSON.stringify(key)},`);
  }
  lines.push("} as const;");
  lines.push(
    "export type ServiceKeyValue = (typeof ServiceKey)[keyof typeof ServiceKey];",
  );
  lines.push("");

  // ─── ServiceMap ───────────────────────────────────────
  lines.push("export type ServiceMap = {");
  for (const [key, { req, res }] of Object.entries(xc.services)) {
    const r = payloadType(req);
    const s = payloadType(res);
    lines.push(`  ${JSON.stringify(key)}: { req: ${r}; res: ${s} };`);
  }
  lines.push("};");
  lines.push("");

  writeFileSync(OUT_PATH, lines.join("\n") + "\n", "utf-8");
  console.log(
    `[gen-contract] ${OUT_PATH} 생성 — ` +
      `topics: ${Object.keys(xc.topics).length}, ` +
      `binary: ${xc.binary_topics.length}, ` +
      `services: ${Object.keys(xc.services).length}`,
  );
}

main().catch((err) => {
  console.error("[gen-contract] 실패:", err);
  process.exit(1);
});
